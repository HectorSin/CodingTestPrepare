# [백준 1436] 영화감독 숌 - Refactoring Feedback
# 문제 링크: https://www.acmicpc.net/problem/1436
#
# [문제 분석]
# 1. 목표: N번째 "종말의 수" 찾기
# 2. "종말의 수" 정의: '666'이 적어도 3개 이상 **연속으로** 들어가는 수
#    - 예: 666, 1666, 2666 (O)
#    - 예: 60606 (X) - 6이 3개지만 연속되지 않음
# 3. 입력: N (1 <= N <= 10,000)
# 4. 시간 복잡도: 제한시간 2초.
#    - 정답은 대략 2,700,000 정도까지 갈 수 있음.
#    - 1부터 1씩 증가시키며 문자열 변환 후 검사해도 충분히 통과 가능.
#    - 복잡도: O(Answer_Value * log10(Answer_Value)), log10은 자릿수(길이)
#
# [개선 포인트]
# 1. 논리 오류 (Logical Error):
#    - 현재 코드는 숫자에 포함된 '6'의 개수가 3개 이상이면 카운트합니다.
#    - 하지만 문제는 "연속된 666"을 요구합니다.
#    - 예: 60606 -> 현재 코드는 정답으로 인정하지만, 실제로는 아님.
# 2. 해결 방안:
#    - `'666' in str(number)` 조건을 사용하면 간단하고 정확하게 구현 가능합니다.

N = int(input())

count = 1
number = 666

while True:
    if count == N:
        break
    number += 1
    if "666" in str(number):
        count += 1

print(number)