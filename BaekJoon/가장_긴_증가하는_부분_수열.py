"""
증가하는 수열에 대한 이해 실수
즉 어떻게든 증가가 되게 수열을 만든다면 가장 긴 수열의 길이 구하는 문제
결국 dp를 사용해서 해당 숫자를 사용했을때 만들어지는 수열 개수를 점진적으로 저장하는 방식으로 접근

1. 우선 앞에 현재 숫자보다 작은 숫자 인덱스 구하기
2. 해당 숫자들 중 dp값이 가장 큰 수 + 1 을 현재 dp 인덱스에 저장
"""

N = int(input())
A_list = list(map(int,input().split()))

dp = [0] * N
dp[0] = 1
max_num = 1

for i in range(1,N):
    #  i보다 작은 수들 중 현재 A_list[i]보다 작은 수들 인덱스 리스트 구하기
    check_num = 1
    for j in range(0,i):
        if A_list[j] < A_list[i]:
            check_num = max(check_num, dp[j] + 1)
    dp[i] = check_num
    max_num = max(max_num, check_num)

print(max_num)

