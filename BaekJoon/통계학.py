import sys
# from collections import Counter # ğŸ’¡ íŒíŠ¸: collections.Counterë¥¼ ì“°ë©´ ìµœë¹ˆê°’ì„ ë§¤ìš° ì‰½ê²Œ êµ¬í•  ìˆ˜ ìˆìŠµë‹ˆë‹¤.

input = sys.stdin.readline

N = int(input()) # num ê°œìˆ˜ (í™€ìˆ˜)
num_list = []

for _ in range(N):
    number = int(input())
    num_list.append(number)

# 1. ì •ë ¬ (ì¤‘ì•™ê°’ ë° ë²”ìœ„ë¥¼ ìœ„í•´ í•„ìš”)
# ì‹œê°„ ë³µì¡ë„: O(N log N) -> 500,000ê°œ ì •ë ¬ì— ì•½ 0.5ì´ˆ ì†Œìš” (ì¶©ë¶„í•¨)
num_list.sort()

# ì‚°ìˆ  í‰ê· 
# ğŸ’¡ ìˆ˜ì •: int(val + 0.5)ëŠ” ìŒìˆ˜ì¼ ë•Œ ì˜¤ì‘ë™í•©ë‹ˆë‹¤. (ì˜ˆ: -1.5 -> -1, ê¸°ëŒ€ê°’ -2)
# ë”°ë¼ì„œ ì–‘ìˆ˜ì¼ ë•ŒëŠ” +0.5, ìŒìˆ˜ì¼ ë•ŒëŠ” -0.5ë¥¼ í•´ì£¼ëŠ” ë°©ì‹ì´ ì•ˆì „í•©ë‹ˆë‹¤.
avg = sum(num_list) / N
if avg >= 0:
    print(int(avg + 0.5))
else:
    print(int(avg - 0.5)) 

# ì¤‘ì•™ê°’
print(num_list[N // 2])

# ìµœë¹ˆê°’
# ğŸ’¡ ê°œì„  ì œì•ˆ: ì§ì ‘ êµ¬í˜„ë„ í›Œë¥­í•˜ì§€ë§Œ, Counterë¥¼ í™œìš©í•˜ë©´ ì½”ë“œê°€ í›¨ì”¬ ê°„ê²°í•´ì§‘ë‹ˆë‹¤.
# count = Counter(num_list).most_common()
# ì´ë¥¼ ì‚¬ìš©í•˜ë©´ ë¹ˆë„ìˆ˜ê°€ ë†’ì€ ìˆœ, ê°’ì´ ì‘ì€ ìˆœìœ¼ë¡œ ì •ë ¬í•˜ê¸° ì‰½ìŠµë‹ˆë‹¤.

# ê¸°ì¡´ ë¡œì§ ìœ ì§€ ë° ìˆ˜ì • (ì§ì ‘ êµ¬í˜„ ì‹œ)
num_hash = [0] * 8001
for n in num_list:
    num_hash[n + 4000] += 1

max_count = max(num_hash)
candidates = []

for idx, count in enumerate(num_hash):
    if count == max_count:
        candidates.append(idx - 4000)

# candidatesëŠ” ì´ë¯¸ idx ìˆœì„œëŒ€ë¡œ(ì‘ì€ ê°’ë¶€í„°) ë“¤ì–´ê°‘ë‹ˆë‹¤.
if len(candidates) > 1:
    print(candidates[1]) # ë‘ ë²ˆì§¸ë¡œ ì‘ì€ ê°’
else:
    print(candidates[0])

# ë²”ìœ„
# ğŸ’¡ ìµœì í™”: ì •ë ¬ëœ ë¦¬ìŠ¤íŠ¸ì˜ ì–‘ ëê°’ì„ ì´ìš©í•˜ë©´ O(1)ì…ë‹ˆë‹¤.
print(num_list[-1] - num_list[0])
