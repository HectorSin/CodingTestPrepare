# 1. ì‚°ìˆ í‰ê· : Pythonì˜ `round()`ëŠ” 'ë°˜ì˜¬ë¦¼' ì‹œ ì§ìˆ˜ ìª½ìœ¼ë¡œ ì´ë™í•˜ëŠ” 'Banker's Rounding'ì„ ì‚¬ìš©í•©ë‹ˆë‹¤.
