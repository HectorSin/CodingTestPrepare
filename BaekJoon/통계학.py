# 2. ìµœë¹ˆê°’: ì¹´ìš´íŒ… ì •ë ¬ ë°©ì‹(ê³„ìˆ˜ ì •ë ¬)ì„ ì˜ ì‚¬ìš©í•˜ì…¨ìŠµë‹ˆë‹¤.
#           ë‹¤ë§Œ `second_index = 0` ì´ˆê¸°í™”ëŠ” ìœ„í—˜í•  ìˆ˜ ìˆìŠµë‹ˆë‹¤. (0ì€ ì‹¤ì œ ê°’ -4000ê³¼ ë§¤í•‘ë˜ë¯€ë¡œ í˜¼ë™ ê°€ëŠ¥)
#           ìµœë¹ˆê°’ í›„ë³´ë“¤ì„ ëª¨ì€ ë’¤ ì •ë ¬í•˜ì—¬ ì²˜ë¦¬í•˜ëŠ” ë°©ì‹ì´ ë” ì§ê´€ì ì¼ ìˆ˜ ìˆìŠµë‹ˆë‹¤.
# 3. ë²”ìœ„: ì´ë¯¸ `num_list`ë¥¼ ì •ë ¬í–ˆìœ¼ë¯€ë¡œ `max()`, `min()`ì„ ë‹¤ì‹œ ì“¸ í•„ìš” ì—†ì´ `num_list[-1] - num_list[0]`ë¥¼ ì“°ë©´ ë” ë¹ ë¦…ë‹ˆë‹¤.
