N = int(input()) # Pn의 N 1000000 -> O(n^2) 불가
S_len = int(input())
S = input()

count = 0
i = 0

while i < S_len - 2:
    if S[i:i+3] == "IOI":
        ioi_pattern = 0

        while i < S_len -2 and S[i:i+3] == "IOI":
            ioi_pattern += 1

            if ioi_pattern >= N:
                count += 1
            
            i += 2
        
        i += 1
    else:
        i += 1

print(count)