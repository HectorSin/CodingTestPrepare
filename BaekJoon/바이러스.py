# 1s, 128mb
# λ¬Έμ  λ¶„μ„: 
# λ°”μ΄λ¬μ¤μ— κ±Έλ¦° μ»΄ν“¨ν„°(1λ²)λ¥Ό ν†µν•΄ μ› λ°”μ΄λ¬μ¤μ— κ±Έλ¦¬κ² λλ” μ»΄ν“¨ν„°μ μλ¥Ό μ¶λ ¥ν•λ” λ¬Έμ μ…λ‹λ‹¤.
# κ·Έλν”„ νƒμƒ‰(BFS λλ” DFS)μ„ ν†µν•΄ 1λ²κ³Ό μ—°κ²°λ λ¨λ“  λ…Έλ“λ¥Ό μ°Ύμ•„μ•Ό ν•©λ‹λ‹¤.
# π’΅ ν•µμ‹¬ ν¬μΈνΈ:
# 1. λ„¤νΈμ›ν¬ μ—°κ²°μ€ 'μ–‘λ°©ν–¥'μ…λ‹λ‹¤. (1-2 μ—°κ²°μ€ 1->2, 2->1 λ¨λ‘ κ°€λ¥)
# 2. 1λ² μ»΄ν“¨ν„° μμ‹ μ€ κ°μ—Όλ μμ—μ„ μ μ™Έν•΄μ•Ό ν•©λ‹λ‹¤.

from collections import deque
import sys
input = sys.stdin.readline

# μ»΄ν“¨ν„° μ 1~100 (N)
N = int(input())

# μ—°κ²° μ (M)
M = int(input())

# π“ λ¦¬ν©ν† λ§ ννΈ 1: μΈμ ‘ λ¦¬μ¤νΈ μ΄κΈ°ν™”
# ν„μ¬ [[0], [1], [2]...] ν•νƒμΈλ°, λ³΄ν†µμ€ λΉ λ¦¬μ¤νΈ [[], [], ...] λ΅ μ΄κΈ°ν™”ν•λ” κ²ƒμ΄ λ” μΌλ°μ μ…λ‹λ‹¤.
con_list = [[] for _ in range(N+1)]

for _ in range(M):
    start, target = map(int,input().split())
    # π“ λ¦¬ν©ν† λ§ ννΈ 2: μ–‘λ°©ν–¥ μ—°κ²°
    # λ¬Έμ μ—μ„ μ—°κ²°μ€ 'μ'μΌλ΅ μ£Όμ–΄μ§‘λ‹λ‹¤. μ¦‰, μ–‘μ½ λ°©ν–¥μΌλ΅ μ΄λ™μ΄ κ°€λ¥ν•΄μ•Ό ν•©λ‹λ‹¤.
    # ν„μ¬λ” start -> target λ§ μ—°κ²°ν•κ³  μμµλ‹λ‹¤. target -> start λ„ μ¶”κ°€ν•΄μ•Ό ν•©λ‹λ‹¤.
    con_list[start].append(target)
    con_list[target].append(start)

queue = deque()

queue.append(1) # νμ— μ²« μ‹μ‘μΈ 1 λ„£μ

visited = [0] * (N+1) # λ°©λ¬Έν–λ”μ§€ μ²΄ν¬
visited[1] = 1

# π“ λ¦¬ν©ν† λ§ ννΈ 3: BFS λ΅μ§ μμ •
# ν„μ¬ λ΅μ§μ€ νμ—μ„ κΊΌλ‚Έ λ…Έλ“μ 'μ΄μ›ƒμ μ΄μ›ƒ'μ„ νμ— λ„£κ³  μμµλ‹λ‹¤.
# ν‘μ¤€μ μΈ BFSλ” λ‹¤μκ³Ό κ°™μµλ‹λ‹¤:
# 1. νμ—μ„ λ…Έλ“λ¥Ό ν•λ‚ κΊΌλ‚Έλ‹¤ (current)
# 2. currentμ™€ μ—°κ²°λ λ¨λ“  μ΄μ›ƒ(next)μ„ ν™•μΈν•λ‹¤.
# 3. λ°©λ¬Έν•μ§€ μ•μ€ μ΄μ›ƒμ΄λ©΄ λ°©λ¬Έ ν‘μ‹λ¥Ό ν•κ³  νμ— λ„£λ”λ‹¤.
while queue:
    next_check = queue.popleft() # νμ—μ„ κΊΌλ‚Έλ‹¤

    for j in con_list[next_check]: # μ—°κ²°λ μ§€μ λ“¤ μ²΄ν¬
        if visited[j] == 0: # ν•λ‚μ”© λ°©λ¬Έν–λ”μ§€ μ²΄ν¬ λ°©λ¬Έ μ•ν–λ‹¤λ©΄ qμ— λ„£κ³  λ°©λ¬Έν‘μ‹
            queue.append(j)
            visited[j] = 1
        else:
            continue

count = 0

for i in range(1,N+1):
    if visited[i] == 1:
        count+=1

# π“ λ¦¬ν©ν† λ§ ννΈ 4: μ •λ‹µ μ¶λ ¥
# 1λ² μ»΄ν“¨ν„°λ¥Ό ν†µν•΄ κ°μ—Όλ μ»΄ν“¨ν„° μλ¥Ό κµ¬ν•΄μ•Ό ν•λ―€λ΅, 1λ² μ»΄ν“¨ν„° μμ²΄λ” μ μ™Έν•΄μ•Ό ν•©λ‹λ‹¤.
# ν„μ¬ count λ΅μ§μ€ visited[1]=1 μ΄λ―€λ΅ 1λ²λ„ ν¬ν•¨ν•κ³  μμµλ‹λ‹¤.
print(count-1)
