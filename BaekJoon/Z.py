"""
[문제 분석]
- 2^N × 2^N 배열을 Z 순서로 탐색할 때 r행 c열의 방문 순서 구하기
- N ≤ 15 → 배열 전체 생성 불가능

[자료구조 선택]
- 배열 생성 불필요 → 계산만으로 해결

[알고리즘]
1. 현재 영역을 4등분 (사분면 크기: half × half)
2. (r, c)가 어느 사분면에 속하는지 판단
   - 1사분면(왼쪽 위): r < half, c < half → +0
   - 2사분면(오른쪽 위): r < half, c >= half → +half^2
   - 3사분면(왼쪽 아래): r >= half, c < half → +2*half^2
   - 4사분면(오른쪽 아래): r >= half, c >= half → +3*half^2
3. 해당 사분면 내에서 좌표를 재조정하고 반복
4. 영역 크기가 1이 될 때까지 반복

[복잡도]
- 시간: O(N)
- 공간: O(1)
"""

N, r, c = map(int,input().split())

result = 0
size = 2 ** N

while size > 1:
    half = size // 2
    size = size // 2
    
    # 좌측 상단
    if r < half and c < half:
        pass
    # 우측 상단
    elif r < half and c >= half:
        result += (half ** 2)
        c -= half
    # 좌측 하단
    elif r >= half and c < half:
        result += (2 * (half ** 2))
        r -= half
    # 우측 하단
    elif r >= half and c >= half:
        result += (3 * (half ** 2))
        r -= half
        c -= half

print(result)