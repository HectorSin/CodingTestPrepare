# 1s, 256mb

import sys
input = sys.stdin.readline
# π“ μ²΄ν¬ν¬μΈνΈ: Nμ΄ μ‘μ•„μ„ κ΄μ°®μ§€λ§, μ…λ ¥μ΄ λ§μ„ λ• sys.stdin.readlineμ΄ ν•„μμ…λ‹λ‹¤.

N, K = map(int,input().split())

coin_list = []

for _ in range(N):
    coin_list.append(int(input()))

# counter = 0

# # π’΅ ννΈ: λ³„λ„μ λ³€μ `left`λ¥Ό λ§λ“¤μ§€ μ•κ³  `K`λ¥Ό μ§μ ‘ κ°μ†μ‹μΌλ„ λ©λ‹λ‹¤.
