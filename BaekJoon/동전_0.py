# π’΅ μ‹¬ν™” ν•™μµ: λ§μ•½ 'λ°°μ μ΅°κ±΄'μ΄ μ—†λ‹¤λ©΄? (Q&A)
# ------------------------------------------------------------
# λ°°μ μ΅°κ±΄μ΄ μ—†μΌλ©΄(μ: λ™μ „μ΄ 1, 4, 5μ›μ΄κ³  λ©ν‘κ°€ 12μ›μΌ λ•)
# κ·Έλ¦¬λ””λ” 5+5+1+1 (4κ°)λ¥Ό μ„ νƒν•μ§€λ§, μ •λ‹µμ€ 4+4+4 (3κ°)μ…λ‹λ‹¤.
# μ΄λ•λ” 'λ‹¤μ΄λ‚λ―Ή ν”„λ΅κ·Έλλ°(DP)'μ„ μ‚¬μ©ν•μ—¬ λ¨λ“  κ²½μ°λ¥Ό ν™•μΈν•΄μ•Ό ν•©λ‹λ‹¤.
# (μ΄ μ ν•μ€ λ°±μ¤€ 2294λ² 'λ™μ „ 2' λ¬Έμ μ…λ‹λ‹¤.)
#
# [DP ν’€μ΄ μμ‹ μ½”λ“]
#
# 1. DP ν…μ΄λΈ” μƒμ„±
# dp[i] = iμ›μ„ λ§λ“λ”λ° ν•„μ”ν• μµμ† λ™μ „ κ°μ
# μ΄κΈ°κ°’ 100001μ€ 'μ•„μ§ λ§λ“¤ μ μ—†μ(λ¬΄ν•λ€)'μ„ μλ―Έν•©λ‹λ‹¤. (λ¬Έμ μ—μ„ Kκ°€ μµλ€ 1μ–µμ΄μ§€λ§ μμ  μ΄ν•΄λ¥Ό μ„ν•΄ μ λ‹Ήν• ν° μ μ‚¬μ©)
dp = [100001] * (K + 1)

# 0μ›μ„ λ§λ“λ” λ° ν•„μ”ν• λ™μ „μ€ 0κ°μ…λ‹λ‹¤. (κΈ°μ € μ΅°κ±΄)
dp[0] = 0

# 2. λ™μ „ ν•λ‚μ”© ν™•μΈν•λ©° ν…μ΄λΈ” κ°±μ‹  (λ°”ν…€-μ—… λ°©μ‹)
for coin in coin_list:
    # ν•΄λ‹Ή λ™μ „(coin)λ¶€ν„° λ©ν‘ κΈμ•΅(K)κΉμ§€ μ±„μ›λ‚κ°‘λ‹λ‹¤.
    for i in range(coin, K + 1):
        # μ ν™”μ‹: dp[i] = min(ν„μ¬κ°’, (i - coin)μ›μ„ λ§λ“λ” κ°μ + 1κ°)
        # μ¦‰, "ν„μ¬ λ™μ „μ„ ν•λ‚ μΌμ„ λ•κ°€ λ” μ΄λ“μΈκ°€?"λ¥Ό ν™•μΈν•©λ‹λ‹¤.
        dp[i] = min(dp[i], dp[i - coin] + 1)

# κ²°κ³Ό μ¶λ ¥ (μ—¬μ „ν 100001μ΄λΌλ©΄ λ§λ“λ” κ²ƒμ΄ λ¶κ°€λ¥ν• κ²½μ°μ…λ‹λ‹¤)
result = dp[K] if dp[K] != 100001 else -1

print(result)