from collections import deque

def solution(progresses, speeds):
    answer = []
    
    queue = deque()
    
    for i in range(len(progresses)):
        left_amount = 100-progresses[i]
        if left_amount % speeds[i] == 0:
            queue.append(left_amount // speeds[i])
        else:
            queue.append((left_amount // speeds[i])+1)
    
    work_stack = []
    
    while queue:
        now_publish = queue.popleft()
        
        if work_stack:
            if now_publish < work_stack[0]:
                work_stack.append(now_publish)
            else:
                answer.append(len(work_stack))
                work_stack = [now_publish]
        else:
            work_stack = [now_publish]    
    answer.append(len(work_stack))
    return answer

